#include "libgfx.h"

float	length_vect(t_vec3 *in)
{
	return (sqrtf(pow(in->x, 2) + pow(in->y, 2) + pow(in->z, 2)));
}

float	dot_vect(t_vec3 *v1, t_vec3 *v2)
{
	return (v1->x * v2->x + v1->y * v2->y + v1->z * v2->z);
}
